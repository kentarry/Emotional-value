// ===== 角色回覆產生器 =====
// 每角色 × 開頭(20) × 結尾(20) × 安慰/開心(20) × 追問(20) = 160,000+ 組合
function getPersonaResponse(analysis, tier, personaId) {
    const p = PERSONAS[personaId] || PERSONAS.warmheart;
    const {category} = analysis;
    const isPos = ['achievement','happy','praise_seek','accomplished','emotion_happy','gratitude'].includes(category);

    const pick = arr => arr[Math.floor(Math.random()*arr.length)];
    const opener = pick(p.openers);
    const closer = pick(p.closers);
    const content = isPos ? pick(p.cheers) : pick(p.comforts);
    const follow = pick(p.follows);

    if (tier === 'basic') {
        return opener + content + closer;
    }

    if (tier === 'intermediate') {
        return opener + content + closer + '\n\n' + follow;
    }

    // Advanced: 兩句安慰/開心 + 追問
    const content2 = isPos ? pick(p.cheers) : pick(p.comforts);
    return opener + content + closer + '\n\n' + content2 + '\n\n' + follow;
}
