// ===== 回應生成 =====
function buildResponse(category, persona) {
    // 決定語氣類型
    let mood = 'comfort';
    if (['emotion_angry'].includes(category)) mood = 'angry';
    if (['achievement','happy','praise_seek','accomplished','emotion_happy','small_achievement','greeting','gratitude'].includes(category)) mood = 'positive';

    // 取得開場和收尾
    const openerPool = (OPENERS[persona] && OPENERS[persona][mood]) || OPENERS.default[mood] || [''];
    const closerPool = (CLOSERS[persona] && CLOSERS[persona][mood]) || CLOSERS.default[mood] || [''];

    // 取得核心回應
    let corePool;
    // 男/女友的吵架用專屬版
    if ((persona === 'boyfriend' || persona === 'girlfriend') && category === 'rel_fight') {
        corePool = CORE.rel_fight_partner;
    } else if (persona === 'boss') {
        corePool = CORE_BOSS[category] || CORE_BOSS.general;
    } else if (persona === 'team_lead') {
        corePool = CORE_TEAMLEAD[category] || CORE_TEAMLEAD.general;
    } else {
        corePool = CORE[category];
    }

    if (!corePool) corePool = CORE.general;

    // 組合：開場 + 核心內容 + 收尾
    const opener = pick(openerPool);
    const core = pickU(corePool);
    const closer = pick(closerPool);

    return (opener + core + closer).trim();
}

function getTierResponse(analysis, tier, options) {
    options = options || {};
    const persona = options.persona || 'default';
    const { category } = analysis;
    return buildResponse(category, persona);
}
