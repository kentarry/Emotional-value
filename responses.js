function getTierResponse(analysis, tier) {
    const {category} = analysis;
    const pool = CORE[category] || CORE.general;
    const isPos = ['achievement','happy','praise_seek','accomplished','emotion_happy','gratitude'].includes(category);
    const isNeu = ['greeting','general'].includes(category);
    const moodType = isPos ? 'pos' : (isNeu ? 'neu' : 'neg');

    const core = pick(pool);

    if (tier === 'basic') {
        return core;
    }

    if (tier === 'intermediate') {
        const followPool = FOLLOW[moodType] || FOLLOW.neu;
        return core + '\n\n' + pick(followPool);
    }

    // Advanced: core + follow-up + deep
    const followPool = FOLLOW[moodType] || FOLLOW.neu;
    const deepPool = DEEP[moodType] || DEEP.neg;
    return core + '\n\n' + pick(followPool) + pick(deepPool);
}
