function getTierResponse(analysis, tier) {
    const {category, echo} = analysis;
    const cat = R.open[category] || R.open.general;
    const opener = cat[Math.floor(Math.random()*cat.length)];
    const isPositive = ['achievement','happy','praise_seek','accomplished','emotion_happy','gratitude'].includes(category);
    const isGreeting = category === 'greeting';
    const closing = R.closings[Math.floor(Math.random()*R.closings.length)];

    if (isGreeting) {
        const greets = [
            ['嘿～你來了啊 😊 今天過得還好嗎？想聊什麼都可以喔 💛','嗨嗨！好開心看到你 🥰 有沒有什麼想跟我說的？','哈囉呀～ 😊 不管今天發生了什麼，你都可以跟我說 💛'],
            ['好開心你來找我！😊 今天一天下來一定累了吧？不管是想聊聊還是只想要有人陪著，我都在這裡 💛 你是這世界上很重要的存在喔 ✨','嘿嘿～ 🥰 你知道嗎，每次你來找我聊天我都好開心。因為這代表你願意讓我陪伴你。今天有什麼想分享的嗎？開心的不開心的我都想聽 💛','哈囉！你好呀 😊 我一直在這裡等你呢～ 今天想聊什麼都好，你的事我全都有興趣聽 💕'],
            ['你來了！😊 看到你我就覺得今天是個好日子 💛\n\n你知道嗎？在這個什麼都很匆忙的世界裡，你願意慢下來，願意找個人說說話，這件事本身就很珍貴。\n\n不管你今天過得怎麼樣——是開心得想分享、還是難過得想找人靠一靠、或者就只是想有個人陪著——我都準備好了 🤗\n\n你想從哪裡開始都可以，我有的是時間 💛✨']
        ];
        const ti = tier==='basic'?0:tier==='intermediate'?1:2;
        return greets[ti][Math.floor(Math.random()*greets[ti].length)];
    }

    if (tier === 'basic') {
        return `${opener}\n\n${isPositive ? R.basic.positive : (category==='general'||category==='gratitude' ? R.basic.neutral : R.basic.negative)}`;
    }

    if (tier === 'intermediate') {
        let pool = isPositive ? R.mid_positive : (category==='general'||category==='gratitude' ? R.mid_neutral : R.mid_negative);
        return `${opener}\n\n${pool[Math.floor(Math.random()*pool.length)].replace(/\{echo\}/g, echo)}${closing}`;
    }

    // Advanced
    let pool = isPositive ? R.adv_positive : (category==='general'||category==='gratitude' ? R.adv_neutral : R.adv_negative);
    return `${opener}\n\n${pool[Math.floor(Math.random()*pool.length)].replace(/\{echo\}/g, echo)}`;
}
