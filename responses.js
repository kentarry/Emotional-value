function getTierResponse(analysis, tier) {
    const {category, echo} = analysis;
    const cat = R.o[category] || R.o.general;
    const opener = cat[Math.floor(Math.random()*cat.length)];
    const isPos = ['achievement','happy','praise_seek','accomplished','emotion_happy','gratitude'].includes(category);
    const isGreet = category === 'greeting';
    const cl = R.cl[Math.floor(Math.random()*R.cl.length)];

    if (isGreet) {
        const g = [
            ['嗨嗨～今天怎麼樣？😊','你來啦！最近好嗎？💛','哈囉！想聊什麼都可以喔 😊'],
            ['嘿嘿 好開心看到你！😊 最近過得還好嗎？有什麼想跟我說的都可以喔 💛','哈囉呀～你今天做了什麼？跟我分享一下吧 🥰','嗨嗨！我等你好久了～最近有什麼新鮮事嗎？😊'],
            ['你來了！😊 每次跟你聊天都覺得很開心。\n\n今天過得如何？不管好的壞的，我都想聽你說 💛','嘿～看到你就覺得今天是好日子 🥰\n\n你最近有什麼事想聊聊嗎？什麼話題都可以喔 💛']
        ];
        const ti = tier==='basic'?0:tier==='intermediate'?1:2;
        return g[ti][Math.floor(Math.random()*g[ti].length)];
    }

    if (tier === 'basic') {
        const pool = isPos ? R.b_pos : (category==='general'||category==='gratitude' ? R.b_neu : R.b_neg);
        return `${opener}\n\n${pool[Math.floor(Math.random()*pool.length)]}`;
    }

    if (tier === 'intermediate') {
        const pool = isPos ? R.m_pos : (category==='general'||category==='gratitude' ? R.m_neu : R.m_neg);
        return `${opener}\n\n${pool[Math.floor(Math.random()*pool.length)].replace(/\{echo\}/g, echo)}${cl}`;
    }

    // Advanced
    const pool = isPos ? R.a_pos : (category==='general'||category==='gratitude' ? R.a_neu : R.a_neg);
    return `${opener}\n\n${pool[Math.floor(Math.random()*pool.length)].replace(/\{echo\}/g, echo)}`;
}
