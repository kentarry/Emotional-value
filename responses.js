function pick(arr) { return arr[Math.floor(Math.random()*arr.length)]; }
const _usedResponses = new Set();
function pickU(arr) {
    const avail = arr.filter(r => !_usedResponses.has(r));
    const chosen = avail.length > 0 ? pick(avail) : pick(arr);
    _usedResponses.add(chosen);
    if (_usedResponses.size > 200) {
        const it = _usedResponses.values();
        for (let i = 0; i < 60; i++) _usedResponses.delete(it.next().value);
    }
    return chosen;
}

